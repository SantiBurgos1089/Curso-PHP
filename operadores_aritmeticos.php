<?php

$numero_1=2;
$numero_2=5;

# Suma
$resultado_1=$numero_1+$numero_2;
echo $resultado_1;

# Resta
$resultado_2=$numero_2-$numero_1;
echo $resultado_2;

# Multiplicacion
$resultado_3=$numero_1*$numero_2;
echo $resultado_3;

# Division
$resultado_4=100/$numero_2;
echo $resultado_4;

# Potencia/Exponenciación
$resultado_5=$numero_2**$numero_1;
echo $resultado_5;

# Residuo/Division módulo
$resultado_6=10%3;
echo $resultado_6;